package hello;

public class For {

	public static void main(String[] args) {

		int i;
		int x = 10;

		for (i = 0; i <= x; i++) {

			System.out.println("contador: " + i);
		}

		System.out.println("----------------------");

		for (i = 10; i >= 0; i--) {

			System.out.println("contador: " + i);
		}

	}

}
